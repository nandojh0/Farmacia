    public void validar(){
    String dni=txtPass.getText();
    String user=txtUser.getText();
        if (txtUser.getText().equals("")||txtPass.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar los datos");
            txtUser.requestFocus();
        }else{
        ev=vdao.ValidarVendedor(dni, user);
            if (ev.getUser()!= null && ev.getDni()!= null) {
                new vistas.Principal().setVisible(true);  
                dispose();
                
            }else{
            JOptionPane.showMessageDialog(this, "Error al ingresar");
            txtUser.requestFocus();
       
            }
        }
    }
